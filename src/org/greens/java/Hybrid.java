package org.greens.java;

public class Hybrid {
    
	public void types() {
System.out.println("Inheritance");
	}
	public void type1() {
		System.out.println("Single");
	}
	public void type2() {
		System.out.println("Multilevel");
	}
	 public static void main(String[] args) {
		
		 Hybrid a = new Hybrid();
		 
		 a.types();
		 a.type1();
		 a.type2();
	}
}
